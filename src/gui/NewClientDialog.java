    private boolean validateForm() {
        if (isClientCityValid() && isClientDobValid() && isClientFirstNameValid()
                && isClientLastNameValid() && isClientHouseNumberValid()
                && isClientPostCodeValid() && isClientStreetValid()
                && isClientPasswordValid() && isLoginUnique()) {
            lblClientPasswordError.setVisible(false);
            lblEmptyFieldsError.setVisible(false);
            lblInvalidClientLogin.setVisible(false);
            return true;
        } else {
            if (!isClientPasswordValid()) {
                lblClientPasswordError.setVisible(true);
            } else {
                lblClientPasswordError.setVisible(false);
            }

            if (!(isClientCityValid() && isClientDobValid() && isClientFirstNameValid()
                    && isClientLastNameValid() && isClientHouseNumberValid()
                    && isClientPostCodeValid() && isClientStreetValid())) {
                lblEmptyFieldsError.setVisible(true);
            } else {
                lblEmptyFieldsError.setVisible(false);
            }

            if (!isLoginUnique()) {
                lblInvalidClientLogin.setVisible(true);
            } else {
                lblInvalidClientLogin.setVisible(false);
            }
        }
        return false;
    }

    private boolean isClientCityValid() {
        return !"".equals(txtClientCity.getText().trim());
    }

    private boolean isClientDobValid() {
        return clientDob.getDate() != null;
    }

    private boolean isClientFirstNameValid() {
        return !"".equals(txtClientFirstName.getText().trim());
    }

    private boolean isClientLastNameValid() {
        return !"".equals(txtClientLastName.getText().trim());
    }

    private boolean isClientHouseNumberValid() {
        return !"".equals(txtClientHouseNumber.getText().trim());
    }

    private boolean isClientPostCodeValid() {
        return !"".equals(txtClientPostCode.getText().trim());
    }

    private boolean isClientStreetValid() {
        return !"".equals(txtClientStreet.getText().trim());
    }

    private boolean isClientPasswordValid() {
        return new ChangePasswordDialog().isNewPasswordValid(
                txtClientPassword.getText());
    }

    private boolean isLoginUnique() {
        ConnectionProvider conn = new ConnectionProvider();
        List<String> allLogins = conn.getClientLogins();
        String enteredLogin = txtClientLogin.getText();
        for (String login : allLogins) {
            if (login.equals(enteredLogin)) {
                return false;
            }
        }
        return true;
    }

