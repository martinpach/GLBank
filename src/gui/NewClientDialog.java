    private void btnSumbitNewClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumbitNewClientActionPerformed
        if (validateForm()) {
            String lastName = txtClientLastName.getText();
            String firstName = txtClientFirstName.getText();
            String email = txtClientEmail.getText();
            String street = txtClientStreet.getText();
            String numStr = txtClientHouseNumber.getText();
            if (numStr.equals("")) {
                numStr = "0";
            }
            int houseNumber;
            boolean isNumeric = numStr.chars().allMatch(Character::isDigit);
            if (isNumeric) {
                houseNumber = Integer.parseInt(txtClientHouseNumber.getText());
            } else {
                houseNumber = 0;
            }
            String postCode = txtClientPostCode.getText();
            String userName = txtClientLogin.getText();
            
            //casting util date to sql date
            Date dob = clientDob.getDate();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String stringDate = dateFormat.format(dob);
            java.sql.Date sqlDob = java.sql.Date.valueOf(stringDate);
            
            String city = txtClientCity.getText();
            String password = txtClientPassword.getText();
            
            Client client = new Client(0, lastName, firstName, email, street,
                    houseNumber, postCode, userName, false, false, sqlDob, city);
            
            new ConnectionProvider().addClientIntoDatabase(client, password);
            new MainForm(); 
            this.dispose();                 
        }
    }//GEN-LAST:event_btnSumbitNewClientActionPerformed

